package day03_EscapeSequences;

public class triangle_2 {
    public static void main(String[] args) {

        //Escape sequence
        // \n append a new line
        System.out.print("Adam\n");
        System.out.print("Eyup");

        System.out.println();
        // \t paragraph space(tab)

        System.out.print("Adam\tEyup\n");
        // \\ single bf slash

        System.out.println("Computer\\Laptop");
        // \" I want to add " in my string

        //Car("Mercedes")
        System.out.println("Car(\"Mercedes\")");

        System.out.print("*\n* *\n* * *\n* * * *\n* * * * *");

    }
}


